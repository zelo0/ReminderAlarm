package com.example.reminderalarm;

public class AlarmTime {
    private int hour;
    private int minute;


    public AlarmTime(int hour, int minute) {
        this.hour = hour;
        this.minute = minute;
    }

    public int getHour() {
        return hour;
    }

    public int getMinute() {
        return minute;
    }

}
